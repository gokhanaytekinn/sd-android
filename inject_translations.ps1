# Apply translations to each language file
foreach ($lang in $allTranslations.Keys) {
    $path = "c:\projects\sd\sd-android\app\src\main\res\values-$lang\strings.xml"
    if (-not (Test-Path $path)) {
        Write-Host "File not found: $path"
        continue
    }
    
    $lines = Get-Content -Path $path -Encoding UTF8
    $existingKeys = @{}
    foreach ($line in $lines) {
        if ($line -match 'name="([^"]+)"') {
            $existingKeys[$matches[1]] = $true
        }
    }
    
    $toInject = @()
    foreach ($key in $allTranslations[$lang].Keys) {
        if (-not $existingKeys.ContainsKey($key)) {
            $val = $allTranslations[$lang][$key]
            $toInject += "    <string name=`"$key`">$val</string>"
        }
    }
    
    if ($toInject.Count -gt 0) {
        # Rebuild content by inserting before </resources>
        $allContent = $lines -join "`n"
        $insertBlock = $toInject -join "`n"
        $newContent = $allContent.Replace("</resources>", "$insertBlock`n</resources>")
        Set-Content -Path $path -Value $newContent -Encoding UTF8
        Write-Host "[$lang] Injected $($toInject.Count) missing keys."
    } else {
        Write-Host "[$lang] No missing keys to inject."
    }
}
